package com.ruicai.edu.entity;

public class TreeGrid {
	private String id;
	private String region;
	private String t_name;
	private String s_name;
	private String g_time;
	private String g_eid;
	private String g_mark;
	private String _parentId;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getRegion() {
		return region;
	}
	public void setRegion(String region) {
		this.region = region;
	}
	public String getT_name() {
		return t_name;
	}
	public void setT_name(String tName) {
		t_name = tName;
	}
	public String getS_name() {
		return s_name;
	}
	public void setS_name(String sName) {
		s_name = sName;
	}
	public String getG_time() {
		return g_time;
	}
	public void setG_time(String gTime) {
		g_time = gTime;
	}
	public String getG_eid() {
		return g_eid;
	}
	public void setG_eid(String gEid) {
		g_eid = gEid;
	}
	public String getG_mark() {
		return g_mark;
	}
	public void setG_mark(String gMark) {
		g_mark = gMark;
	}
	public String get_parentId() {
		return _parentId;
	}
	public void set_parentId(String parentId) {
		_parentId = parentId;
	}
	
	
	
}
